public class PierwszyProgram {
    public static void main(String[] args) {


        String hello = "Hello World";
        String przedstawSie = "Mam na imię";
        String imie = "Dobiesław";

        System.out.println(hello);
        System.out.println(przedstawSie);
        System.out.println(imie);


        System.out.println("\nPunkt 3:\n");
        System.out.println(hello + "\n" + przedstawSie + "\n" + imie);

    }

}

